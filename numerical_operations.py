def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def  multiply(a, b):
    return a * b

def power(a, b):
    return a**b

def square(a):
    return a*a

def modulo(a, b):
    if a and b != 0:
        return a % b
    else:
        return 0

def divide(a, b):
    if a and b != 0:
        return a / b
    else:
        return 0

def integer_division(a, b):
    return a // b